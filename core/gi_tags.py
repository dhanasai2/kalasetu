def get_gi_tag(art_form):
    """Look up GI tag info for an art form. Returns dict or None."""
    if not art_form:
        return None
    art_lower = art_form.lower().strip()
    # Direct match
    if art_lower in GI_TAGS:
        return GI_TAGS[art_lower]
    # Partial match
    for key, data in GI_TAGS.items():
        if key in art_lower or art_lower in key:
            return data
    return None
